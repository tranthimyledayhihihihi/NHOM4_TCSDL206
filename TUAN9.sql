		CHECK (DIENTHOAI LIKE '[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]')
--MẶC ĐỊNH LUONGCOBAN LÀ 5000000 VÀ LUONGCOBAN >0
