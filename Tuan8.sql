--BẢNG KHACHHANG
CREATE TABLE KHACHHANG (
    MAKHACHHANG CHAR(10) PRIMARY KEY,
    TENCONGTY NVARCHAR(100) NOT NULL,
    TENGIAODICH NVARCHAR(100) NOT NULL,
    DIACHI NVARCHAR(100) NOT NULL,
    EMAIL VARCHAR(50)  UNIQUE,
    DIENTHOAI VARCHAR(10) UNIQUE,
    FAX NVARCHAR(25)
